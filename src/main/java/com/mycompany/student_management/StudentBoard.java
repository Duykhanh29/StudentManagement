    private void display() {
        if(listStudent.getSize()!=0)
        {
            for(int i=0;i<listStudent.getSize();i++)
            {
                showTable((i+1),listStudent.getStudentAtIndex(i));
            }
        }
    }
    public String getGenderToString(boolean x)
    {
        if(x==true)
        {
            return "Nam";
        }
        return "Nu";
    }
       public String getAverageScore(String idStudent)
    {
        int soTin=listScore.getSOTinOfAStudent(idStudent);
        if(soTin!=0){
            return String.valueOf(listScore.getAverageScoreOfAStudent(idStudent));
        }
        return "";
    }
    public void showTable(int i,Student st)
    {
        defaultTableModel.addRow(new Object[]{
            i,st.getId(),st.getName(),getGenderToString(st.isGender()),st.getAddress(),st.getPhoneNumber(),st.getEmail(),getAverageScore(st.getId())
        });
    }
